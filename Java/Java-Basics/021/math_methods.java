class math_methods{

    public static void main(String args[]) {       

        System.out.println("Math.sqrt :" + Math.sqrt(16));
        System.out.println("Math.sin :" + Math.sin(Math.PI/4));
        System.out.println("Math.cos :" + Math.cos(Math.PI/4));
        System.out.println("Math.pow :" + Math.pow(16,2));
        System.out.println("Math.abs :" + Math.abs(-16));
        System.out.println("Math.max :" + Math.max(16,2));
        System.out.println("Math.min :" + Math.min(16,2));
        System.out.println("Math.floor :" + Math.floor(16.356));
        System.out.println("Math.ceil :" + Math.ceil(16.356));

    }
}