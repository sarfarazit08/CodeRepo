class forloop{

    public static void main(String args[]) {       

        System.out.println("First for loop : ");
        for(int i = 0 ; i <= 5 ; i++){
            System.out.println(" i -> " + i);
        }

        System.out.println("Second for loop : ");
        for(int i = 0 ; i <= 25 ; i += 5){
            System.out.println(" i -> " + i);
        }                      
    }
}
